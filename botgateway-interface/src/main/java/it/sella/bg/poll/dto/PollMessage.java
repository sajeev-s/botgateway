package it.sella.bg.poll.dto;

public class PollMessage {

	String sender;
	String message;

	public String getSender() {
		return this.sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getMessage() {
		return this.message;
	}
	public void setMessage(String message) {
		this.message = message;
	}



}
